# Concatenate using "+" operator.
MyStr1 = "Hello " + "World"  
puts MyStr1

# Concatenate using a single space.  
MyStr2 = "Hello " "World"  
puts MyStr2
  
# Concatenate using "<<" operator.
MyStr3 = "Hello " << "World"  
puts MyStr3
  
# Concatenate using concat() method.
MyStr4 = "Hello ".concat("World")
puts MyStr4
